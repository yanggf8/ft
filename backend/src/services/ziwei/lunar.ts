function getYearDays(year: number): number {
  let sum = 348;
  const info = LUNAR_INFO[year - 1900];
  for (let i = 0x8000; i > 0x8; i >>= 1) {
    sum += (info & i) ? 1 : 0;
  }
  return sum + getLeapDays(year);
}

function getLeapMonth(year: number): number {
  return LUNAR_INFO[year - 1900] & 0xf;
}

function getLeapDays(year: number): number {
  if (getLeapMonth(year)) {
    return (LUNAR_INFO[year - 1900] & 0x10000) ? 30 : 29;
  }
  return 0;
}

function getMonthDays(year: number, month: number): number {
  return (LUNAR_INFO[year - 1900] & (0x10000 >> month)) ? 30 : 29;
}

export function solarToLunar(year: number, month: number, day: number): { year: number; month: number; day: number; isLeap: boolean } {
  // Base: 1900-01-31 is lunar 1900-01-01
  const baseDate = Date.UTC(1900, 0, 31);
  const targetDate = Date.UTC(year, month - 1, day);
  let offset = Math.floor((targetDate - baseDate) / 86400000);
  
  if (offset < 0) {
    return { year: 1900, month: 1, day: 1, isLeap: false };
  }

  // Find lunar year
  let lunarYear = 1900;
  let yearDays = getYearDays(lunarYear);
  while (offset >= yearDays && lunarYear < 2100) {
    offset -= yearDays;
    lunarYear++;
    yearDays = getYearDays(lunarYear);
  }

  // Find lunar month
  const leapMonth = getLeapMonth(lunarYear);
  let lunarMonth = 1;
  let isLeap = false;
  
  for (let i = 1; i <= 12; i++) {
    let days = getMonthDays(lunarYear, i);
    if (offset < days) {
      lunarMonth = i;
      break;
    }
    offset -= days;
    
    // Check leap month
    if (i === leapMonth) {
      days = getLeapDays(lunarYear);
      if (offset < days) {
        lunarMonth = i;
        isLeap = true;
        break;
      }
      offset -= days;
    }
    lunarMonth = i + 1;
  }

  return { year: lunarYear, month: lunarMonth, day: offset + 1, isLeap };
}
